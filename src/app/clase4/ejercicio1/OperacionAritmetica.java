package app.clase4.ejercicio1;

public class OperacionAritmetica {
    public long sumar(long sumando1, long sumando2){
        return sumando1 + sumando2;
    }
    
    public long restar(long minuendo, long sustraendo){
        return minuendo - sustraendo;
    }
    
    public long multiplicacion(long multiplicando, long multiplicador){
        return multiplicando*multiplicador;
    }
    
    public long division(long dividendo, long divisor){
        return dividendo/divisor;
    }
}
