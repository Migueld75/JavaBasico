package app.clase4.ejercicio1.ver2;

public class OperadorAritmetico {

    public static int sumar(int sumando1, int sumando2) throws Exception{
        return sumando1 + sumando2;
    }

    public static int restar(int minuendo, int sustraendo) throws Exception{
        return minuendo - sustraendo;
    }

    public static int multiplicar(int multiplicando, int multiplicador) throws Exception{
        return multiplicando * multiplicador;
    }

    public static int dividir(int dividendo, int divisor) throws Exception{
        return dividendo / divisor;
    }
}
